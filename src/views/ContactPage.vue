<script setup>
import { ref, reactive } from 'vue'

// Form state
const form = reactive({
  name: '',
  email: '',
  subject: '',
  message: ''
})

// Error state
const errors = reactive({
  name: '',
  email: '',
  subject: '',
  message: ''
})

// Other reactive state
const isSubmitting = ref(false)
const formStatus = reactive({
  show: false,
  isError: false,
  message: ''
})

// Email validation function
const validateEmail = (email) => {
  const re = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
  return re.test(email)
}

// Form validation function
const validateForm = () => {
  let isValid = true
  
  // Reset all errors
  Object.keys(errors).forEach(key => {
    errors[key] = ''
  })

  if (!form.name.trim()) {
    errors.name = 'Name is required'
    isValid = false
  }

  if (!form.email.trim()) {
    errors.email = 'Email is required'
    isValid = false
  } else if (!validateEmail(form.email)) {
    errors.email = 'Please enter a valid email'
    isValid = false
  }

  if (!form.message.trim()) {
    errors.message = 'Message is required'
    isValid = false
  } else if (form.message.trim().length < 10) {
    errors.message = 'Message must be at least 10 characters'
    isValid = false
  }

  return isValid
}

// Form submission handler
const submitForm = async () => {
  // Clear previous status
  formStatus.show = false
  
  // Validate the form
  if (!validateForm()) {
    return
  }
  
  isSubmitting.value = true
  
  try {
    // Simulate API call with a delay
    await new Promise(resolve => setTimeout(resolve, 1500))
    
    // In a real application, you would send the form data to your backend
    // const response = await fetch('/api/contact', {
    //   method: 'POST',
    //   headers: { 'Content-Type': 'application/json' },
    //   body: JSON.stringify(form)
    // })
    
    // if (!response.ok) throw new Error('Failed to send message')
    
    // Show success message
    formStatus.show = true
    formStatus.isError = false
    formStatus.message = 'Thank you for your message! I will get back to you soon.'
    
    // Reset form
    form.name = ''
    form.email = ''
    form.subject = ''
    form.message = ''
  } catch (error) {
    // Show error message
    formStatus.show = true
    formStatus.isError = true
    formStatus.message = 'Something went wrong. Please try again later.'
    console.error('Form submission error:', error)
  } finally {
    isSubmitting.value = false
  }
}
</script>

